        <h3 id="02">属性について</h3>
        <div class="content-description">
            <p>
                攻撃時、防御時に属性を指定します。属性の指定により攻撃の成功失敗が判定されます。<br>
                属性は『火』『木』『風』『水』の4つです。<br>
                『火』→『木』→『風』→『水』→『火』の順で優劣が決まります。<br>
            </p>
            <h4>基本的な判定</h4>
            <p>
                基本的に、属性『有利』となれば攻撃が命中しますが、スキルによって判定が変化します。<br>
                詳細は「スキルについて」を参照してください。<br>
                <dl>
                    <dt>●属性『有利』</dt>
                    <dd>
                        攻撃が命中。<br>
                    </dd>
                    <dt>●属性『拮抗』</dt>
                    <dd>
                        (20+敏捷*10)% の確率で攻撃が命中。<br>
                    </dd>
                    <dt>●属性『不利』</dt>
                    <dd>
                        攻撃が失敗。<br>
                    </dd>
                </dl>
            </p>
        </div>
