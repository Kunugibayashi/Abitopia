    <div class="content">
        <h3 id="04">底力について</h3>
        <div class="content-description">
            <p>
                HPが0になった際、ある一定の確率で復活します。<br>
                HP1、SP0、コンボ×0となりますが、覚醒スキルが発動判定が入るため、逆転のチャンスとなります。<br>
                相手がHP1の場合、覚醒スキルは発動しないため、注意してください。<br>
            </p>
            <p>
                底力の発動確率：（SP + コンボ * 2）%<br>
            </p>
        </div>
    </div>
    <div class="content">
        <h3 id="05">かすり傷について</h3>
        <div class="content-description">
            <p>
                攻撃が確率で失敗した場合、同確率でかすり傷のダメージの判定が入ります。<br>
                判定に成功した場合、0.5倍のダメージを与えます。失敗した場合はダメージを与えられません。<br>
            </p>
