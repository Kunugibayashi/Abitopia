                </dd>
                <dt>●必殺技（中）</dt>
                <dd>
                    消費SP3。ダメージ2倍。<br>
                </dd>
                <dt>●必殺技（大）</dt>
                <dd>
                    消費SP4。ダメージ2.5倍。<br>
                </dd>
            </dl>
