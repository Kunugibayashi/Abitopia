        <h3 id="01">スキルについて</h3>
        <div class="content-description">
            <p>
                スキルは、覚醒スキル、パッシブスキル、戦闘スキルに分けられます。
            </p>
            <h4>覚醒スキル</h4>
            <p>
                自PCのHPが、相手のHP半分以下になった場合に発動するスキルです。発動した覚醒スキルは戦闘終了まで継続します。<br>
                発動すれば逆転が起こり得る強力な切り札です。<br>
            </p>
            <dl>
                <dt>●リミットブレイク</dt>
                <dd>
                    腕力+5。<br>
                </dd>
                <dt>●コンセントレイト</dt>
                <dd>
                    SP+4、命中率+20%、回避率+20%。<br>
                </dd>
                <dt>●デュアルドライブ</dt>
                <dd>
                    攻撃命中時、確定で連続攻撃。<br>
                    攻撃を外した場合、補正なしの命中率で連続攻撃。<br>
                </dd>
            </dl>
            <h4>パッシブスキル</h4>
            <p>
                常時発動するスキルです。<br>
                効果は小さいですが、戦略に深く関わります。<br>
            </p>
            <dl>
                <dt>●攻撃力強化</dt>
                <dd>
                    腕力+1。<br>
                </dd>
                <dt>●命中率強化</dt>
                <dd>
                    命中率+10%。<br>
                </dd>
                <dt>●SP強化</dt>
                <dd>
                    初期SP+3。<br>
                </dd>
                <dt>●コンボ充填</dt>
                <dd>
                    初期コンボ+2。<br>
                </dd>
            </dl>
            <h4>戦闘スキル</h4>
            <p>
                攻撃ターン、防御ターンで使用可能なスキルです。戦闘開始時に7つのスキルを選択します。<br>
                戦闘スキルは、さらに3つに分類することができます。<br>
                <dl>
                    <dd>○攻撃ターンで使用可能な技スキル</dd>
                    <dd>◉攻撃ターンで使用可能な特殊スキル</dd>
                    <dd>■防御ターンで使用可能な防御スキル</dd>
                </dl>
            </p>
            <dl>
                <dt>○精密射撃</dt>
                <dd>
                    攻撃属性を一致させた場合、その攻撃のみ命中率+35%。<br>
                </dd>
                <dt>○部位破壊</dt>
                <dd>
                    攻撃属性を一致させた場合、その攻撃のみ腕力+2。<br>
                </dd>
                <dt>○ガードブレイク</dt>
                <dd>
                    相手の【攻防一体】の発動を阻害。<br>
                    相手が【防御専念】の場合、属性『不利』と『拮抗』が命中。『有利』は相殺される。<br>
                    相手が【回避専念】の場合、属性『不利』が命中。『拮抗』と『有利』は回避される。<br>
                    上記以外の場合、属性『不利』が命中、『拮抗』が確率命中、『有利』が相殺される。<br>
                </dd>
                <dt>○ホーミング</dt>
                <dd>
                    相手の【カウンター】の発動を阻害。<br>
                    相手が【乱数回避】の場合、どの属性も命中。<br>
                    相手が【回避専念】の場合、属性『不利』と『拮抗』が命中。『有利』は相殺される。<br>
                    相手が【防御専念】の場合、同属性のみ命中。<br>
                    上記以外の場合、同属性のみ命中、その他の『拮抗』が確率命中、『不利』と『有利』が防御される。<br>
                </dd>
                <dt>○コンビネーション</dt>
                <dd>
                    その攻撃のみ、腕力が（コンボ数+1）上昇、命中率が（コンボ数*10）%上昇。<br>
                    コンボがない場合、不発。<br>
                </dd>
                <dt>○明鏡止水</dt>
                <dd>
                    必殺技の使用時に発動可能。<br>
                    相手が【覚悟完了】の場合、（ダメージ*2）。<br>
                    相手が【覚悟完了】の以外の場合、（ダメージ*0.75）。<br>
                    必殺技を使用するSPがない場合、不発。<br>
                </dd>
                <dt>◉戦意高揚</dt>
                <dd>
                    （コンボ数*4）のHP回復。<br>
                    コンボがない場合、不発。<br>
                </dd>
                <dt>◉精神統一</dt>
                <dd>
                    （コンボ数*2）のSP回復。<br>
                    コンボがない場合、不発。<br>
                </dd>
                <dt>■防御専念</dt>
                <dd>
                    属性『拮抗』時、回避率+25%。<br>
                    相手が【ガードブレイク】の場合、攻撃を避けにくい。詳細は【ガードブレイク】を参照。<br>
                </dd>
                <dt>■回避専念</dt>
                <dd>
                    属性『拮抗』時、回避率+25%。<br>
                    相手が【ホーミング】の場合、攻撃を避けにくい。詳細は【ホーミング】を参照。<br>
                </dd>
                <dt>■乱数回避</dt>
                <dd>
                    全ての属性で、属性『拮抗』と同様の回避率になる。<br>
                    属性『拮抗』時、回避率+15%。<br>
                    相手が【ホーミング】の場合、攻撃を避けられない。<br>
                </dd>
                <dt>■攻防一体</dt>
                <dd>
                    相手の攻撃を防御した場合、次ターンで腕力+2。<br>
                    相手が【ガードブレイク】の場合、発動しない。<br>
                </dd>
                <dt>■カウンター</dt>
                <dd>
                    相手の攻撃を回避した場合、次ターンで腕力+1。<br>
                    属性『拮抗』時、回避率+15%。<br>
                    相手が【ホーミング】の場合、発動しない。<br>
                </dd>
                <dt>■覚悟完了</dt>
                <dd>
                    相手が必殺技の場合、（ダメージ*0.5）。<br>
                    相手が【明鏡止水】の場合、（ダメージ*2）。<br>
                </dd>
            </dl>
        </div>
